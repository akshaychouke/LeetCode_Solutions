class Solution {
  public:
    string removeOuter(string& s) {
        // code here
        int opened = 0;
        string ans = "";
        for(char ch:s){
            if(ch == '(' && opened++ > 0){
                ans +=ch;
            }
            if(ch == ')' && opened-->1){
                ans+=ch;
            }
        }
        
        return ans;
    }
};

