class Solution {
  public:
    // Function to return the adjacency list for each vertex.
    vector<vector<int>> printGraph(int V, vector<pair<int,int>>edges) {
        // Code here
        vector<vector<int>> ans(V);
        int n = edges.size();
        
        for(int i =0;i<n;i++){
            pair<int,int> p = edges[i];
            
            int first1 = p.first;
            int second2 = p.second;
            
            ans[first1].push_back(second2);
            ans[second2].push_back(first1);
        }
        
        return ans;
        
    }
};

